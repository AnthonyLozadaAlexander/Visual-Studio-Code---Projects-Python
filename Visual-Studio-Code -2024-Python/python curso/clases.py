# CLASES

class aHuman:
    def __init__(self, altura, edad, peso):
        self.altura = altura
        self.edad = edad
        self.peso = peso

human_1 = aHuman(1.80, 23, 87)

print(f"el humano 1 mide {human_1.altura}, pesa {human_1.peso}kg y tiene {human_1.edad} años")



